package cn.bean;

public class Seat 
{
	private int id;
	private String name;
	public Seat() {
		super();
	}
	public Seat(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
